package assignments;

public class AssignmentDay8_CalculatorClass {

	// Addition Method
	public int add(int input1, int input2) {
		int add = input1 + input2;
		return add;
	}

	// Subtraction Method
	public int diff(int input1, int input2) {
		int diff = input1 + input2;
		return diff;
	}

	// Multiplication Method
	public int product(int input1, int input2) {
		int product = input1 * input2;
		return product;
	}

	// Quotient Method
	public int quotient(int input1, int input2) {
		int quotient = input1 / input2;
		return quotient;
	}

	// Remainder Method
	public int remainder(int input1, int input2) {
		int remainder = input1 % input2;
		return remainder;
	}

}
